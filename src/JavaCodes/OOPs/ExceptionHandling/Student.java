package JavaCodes.OOPs.ExceptionHandling;

public class Student {
    private int id;
    private int name;

    public Student(int id, int name) {
        this.id = id;
        this.name = name;
    }

    public int getId() {
        return id;
    }

    public void setId(int id) {
        this.id = id;
    }

    public int getName() {
        return name;
    }

    public void setName(int name) {
        this.name = name;
    }
}
